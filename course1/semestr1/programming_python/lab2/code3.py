A = float(input("Введіть значення A: "))
S = 0
N = 1

if A > 1: #перевіряю на відповідність значення A до одиниці

    while S < A or S == A: #цикл повторюється доки S менше або дорівнює A

        S += 1/N #додаю до минулого значення суми 1/N
        N += 1 #збільшую значення N на 1

    else:

        print("N =", N)
        print("S =", S)

else:

    print("Введене значення A менше або дорівнює одиниці")