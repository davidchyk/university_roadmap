# Дійсні значення ємності
real_C_list = [0.165e-6, 0.327e-6, 0.877e-6, 1.123e-6]

# Виміряні значення ємності
expr_C_list = [1.656e-7, 3.3e-7, 8.989e-7, 1.114e-6]

for i in range(0, 4):

    relative_mistake = abs(expr_C_list[i] - real_C_list[i]) / expr_C_list[i] * 100 # Розрахунок значення відносної похибки
    print(f"Відносна похибка значень експериментльного значення ⟨C{i+1}⟩ від теоретичного: {relative_mistake:.2f}%") #  Вивід з округленням до двох знаків після коми